/* ------------------------------------------- Dependencies Import Section ---------------------------------- */

import mongoose from "mongoose";

/* ------------------------------------------- Doctor Schema Section ----------------------------------------- */

const UserSchema = mongoose.Schema({
    id: {
        type: String,
        required: true,
        unique: true,
    },
    username: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
        select: false,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    }
});

export const User = mongoose.model("User", UserSchema);